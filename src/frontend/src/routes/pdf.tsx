    setLoading(true);
    setRobotState("thinking");
    await new Promise((r) => setTimeout(r, 2200));
    const result = generatePDFContent(topic);
    setDoc(result);
    const built = buildPages(result.sections);
    setPages(built);
    setCurrentPage(0);
    setLoading(false);

