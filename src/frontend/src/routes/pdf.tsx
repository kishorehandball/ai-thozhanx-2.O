// ── Page grouping ─────────────────────────────────────────────────────────────

function buildPages(sections: PDFSection[]): PDFSection[][] {
  const pages: PDFSection[][] = [];
  for (let i = 0; i < sections.length; ) {
    const curr = sections[i];
    if (curr.type === "cover" || curr.type === "highlight") {
      pages.push([curr]);
      i++;
    } else if (i + 1 < sections.length) {
      pages.push([curr, sections[i + 1]]);
      i += 2;
    } else {
      pages.push([curr]);
      i++;
    }
  }
  return pages;
}

